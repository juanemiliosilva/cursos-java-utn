
package tp2;


public class Ejercicio3 {
    public static void main(String[] args) {
        int N = 40;
        System.out.println("Valor inicial de N: "+ N);
        N+=77;
        System.out.println("N + 77= " + N);
        N-=3;
        System.out.println("N - 3= " + N);
        N*=2;
        System.out.println("N duplicada= " + N);
                
    }
    
}
